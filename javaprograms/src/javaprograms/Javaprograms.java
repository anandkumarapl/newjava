package javaprograms;

public class Javaprograms {

    public static void main(String[] args) {
     for(int i=0;i<=4;i++){
         for(int s=i;s<=4;s++){
             System.out.print(" ");
         }
         for(int j=1;j<=i;j++){
             System.out.print("*"+" ");
         }
         System.out.println(" ");    
    }
     }
}